    /**
     * Afficher les produit dont le stock est en dessous de 10.
     */
    public void AfficherListProduitEnRutpure(){
DefaultListModel leModelProduitRupture = (DefaultListModel) jListProduitRupture.getModel();
        //clear des model des listes
        leModelProduitRupture.clear();
        //Requete qui nous donne le nom du produit en fonction de la Recherche pr√©cedante et du nom du produit
        String sqlProduitEnRupture = "SELECT * FROM produit ";
        ResultSet TuplesProduitEnRupture = DaoSIO.getInstance().requeteSelection(sqlProduitEnRupture);
        try {
            while (TuplesProduitEnRupture.next()) {
                Integer stock =Integer.parseInt(TuplesProduitEnRupture.getString("stock"));
                if (stock <= 10) {
                    leModelProduitRupture.addElement(TuplesProduitEnRupture.getString("libelleProduit"));//ajoute la categorie a la liste
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(SelectionProduit.class.getName()).log(Level.SEVERE, null, ex);
        }
}
/**
 * Affiche le Chiffre D'affaire des Vente 
 */
public void AfficherChiffreDAffaire(){
String sqlChiffreAffaire ="SELECT SUM(facture) FROM bon_commande";
        ResultSet TuplesChiffreAffaire = DaoSIO.getInstance().requeteSelection(sqlChiffreAffaire);
        try {
            while (TuplesChiffreAffaire.next()) {
                jLabelChiffreAffaire.setText(TuplesChiffreAffaire.getString("SUM(facture)"));
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdministrationStatistic.class.getName()).log(Level.SEVERE, null, ex);
        }
}


/**
 * Permet d'afficher la liste et le Chiffred'affaire
 */
public void ToutAfficher(){
AfficherChiffreDAffaire();
AfficherListProduitEnRutpure();
}
